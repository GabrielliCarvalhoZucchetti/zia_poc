// Defined ROLE_HIERARCHY to define numeric weights for each UserRole.
export const ROLE_HIERARCHY: Record<string, number> = {
  [UserRole.BASIC]: 1,
  [UserRole.INTERMEDIATE]: 2,
  [UserRole.ADVANCED]: 3,
  [UserRole.ADMINISTRATOR]: 4,
};

// canUserAccessResource checks if a user's role meets the minimum required role for a resource.
export const canUserAccessResource = (userRole: UserRole, resourceRole: UserRole): boolean => {
  return ROLE_HIERARCHY[userRole] >= ROLE_HIERARCHY[resourceRole];
};
